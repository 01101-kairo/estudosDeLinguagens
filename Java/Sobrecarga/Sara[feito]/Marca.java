public class Marca {
    private String samsung;
    private String xiaomi;
    private String oneplus;
    private String motorola;

    public Marca(String samsung, String xiaomi, String oneplus, String motorola) {
        this.samsung = samsung;
        this.xiaomi = xiaomi;
        this.oneplus = oneplus;
        this.motorola = motorola;
    
    }
    public String getsamsung() {
        return samsung;
    }
    public void setsamsung(String samsung) {
        this.samsung = samsung;
    }
    public String getxiaomi() {
        return xiaomi;
    }
    public void setxiaomi(String xiaomi) {
        this.xiaomi = xiaomi;
    }
    public String getoneplus() {
        return oneplus;
    }
    public void setoneplus(String oneplus) {
        this.oneplus = oneplus;
    }
    public String getmotorola() {
        return motorola;
    }
    public void setmotorola(String motorola) {
        this.motorola = motorola;
    }

}
