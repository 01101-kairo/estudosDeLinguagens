public class Tipo {
	private String adidas;
	private String acostamento;

	public Tipo(String adidas, String acostamento) {
		this.adidas = adidas;
		this.acostamento = acostamento;

	}
	public String getAdidas() {
		return adidas;
	}
	public void setAdidas(String adidas) {
		this.adidas = adidas;
	}
	public String getAcostamento() {
		return acostamento;
	}
	public void setAcostamento(String acostamento) {
		this.acostamento = acostamento;
	}
}
