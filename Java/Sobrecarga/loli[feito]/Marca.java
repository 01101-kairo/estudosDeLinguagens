public class Marca {
    private String Volkswagen;
    private String Toyota;
    private String Hyundai;
    private String Nissan;

    public Marca(String Volkswagen, String Toyota, String Hyundai, String Nissan) {
        this.Volkswagen = Volkswagen;
        this.Toyota = Toyota;
        this.Hyundai = Hyundai;
        this.Nissan = Nissan;
    }
    public String getVolkswagen() {
        return Volkswagen;
    }
    public void setVolkswagen(String Volkswagen) {
        this.Volkswagen = Volkswagen;
    }
    public String getToyota() {
        return Toyota;
    }
    public void setToyota(String Toyota) {
        this.Toyota = Toyota;
    }
    public String getHyundai() {
        return Hyundai;
    }
    public void setHyundai(String Hyundai) {
        this.Hyundai = Hyundai;
    }
    public String getNissan() {
        return Nissan;
    }
    public void setNissan(String Nissan) {
        this.Nissan = Nissan;
    }
}
