/*• Sem utilizar a operação de multiplicação, escreva um programa que
multiplique dois números inteiros. Por exemplo: 2 * 2 = 2 + 2.*/
#include <stdio.h>
#include <stdlib.h>

int main() {
  int n1, n2, r, s;
  n1 = n2 = r = 0;
  printf("insira valor: ");
  scanf("%d", &n2);
  printf("insira valor: ");
  scanf("%d", &n1);
  for (s = 1; s <= n2; s++) {
    r = r + n1;
  }
  printf("\n \n");
  printf("%d", r);
}
